let tenis = { // Para criar um objeto, é necessário a utilização de chaves {conteúdo}
    cor: "branco",  // chave: valor
    tamanho: "42",
    emEstoque: true,
}

tenis.valor = 200 // Atribui uma nova caracterítica ao objeto.

console.log(tenis); // Vai mostrar um Object, com as propriedades e valores internos
console.log(tenis.cor); // Para verificar a propriedade do objeto
console.log(tenis["cor"]); // Forma alternativa de poder visualizar a propriedade

