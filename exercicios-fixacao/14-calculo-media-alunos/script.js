// Lê as duas notas
let nota1 = Number(prompt("Qual a primeira nota? "));
let nota2 = Number(prompt("Qual a segunda nota? "));
// Calcula a média
let media = (nota1 + nota2) / 2;
// Mostra o resultado
alert("1ª Nota: " + nota1.toFixed(1) + "\n2ª Nota: " + nota2.toFixed(1) + "\nMédia: " + media.toFixed(1));
